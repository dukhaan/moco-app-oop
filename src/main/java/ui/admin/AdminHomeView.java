    private void btnEditBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditBookActionPerformed
        try {
            // Dapatkan data yang diedit dari kolom teks
            String editedTitle = lblTitle.getText();
            String editedAuthor = lblAuthor.getText();
            double editedPrice = Double.parseDouble(lblPrice.getText());

            int selectedRow = bookTable.getSelectedRow();
            if (selectedRow != -1) {
                // Perbarui buku yang dipilih di tabel
                bookTable.setValueAt(editedTitle, selectedRow, 0);
                bookTable.setValueAt(editedAuthor, selectedRow, 1);
                bookTable.setValueAt(editedPrice, selectedRow, 2);

                List<Book> bookList = mainFrame.getStore().getBooks();
                Book selectedBook = bookList.get(selectedRow);

                selectedBook.setTitle(editedTitle);
                selectedBook.setAuthor(editedAuthor);
                selectedBook.setPrice(editedPrice);

                JOptionPane.showMessageDialog(this, "Edit buku berhasil!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Harap pilih buku untuk diedit.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Format harga tidak valid. Harap masukkan angka yang valid.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengedit buku.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }

        // Bersihkan kolom teks
        lblTitle.setText("");
        lblAuthor.setText("");
        lblPrice.setText("");
    }//GEN-LAST:event_btnEditBookActionPerformed

    private void btnDeleteBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteBookActionPerformed
        int selectedRow = bookTable.getSelectedRow();
        if (selectedRow != -1) {
            // Dapatkan buku yang dipilih dari model tabel
            DefaultTableModel model = (DefaultTableModel) bookTable.getModel();
            String deletedTitle = (String) model.getValueAt(selectedRow, 0);

            // Hapus baris yang dipilih dari tabel
            model.removeRow(selectedRow);

            // Hapus buku yang sesuai dari data model atau struktur data
            List<Book> bookList = mainFrame.getStore().getBooks();
            for (int i = 0; i < bookList.size(); i++) {
                Book book = bookList.get(i);
                if (book.getTitle().equals(deletedTitle)) {
                    bookList.remove(i);
                    break;
                }
            }

            // Tampilkan pesan sukses
            JOptionPane.showMessageDialog(this, "Buku '" + deletedTitle + "' berhasil dihapus.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Tampilkan pesan kesalahan jika tidak ada baris yang dipilih
            JOptionPane.showMessageDialog(this, "Harap pilih buku untuk dihapus.", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteBookActionPerformed

