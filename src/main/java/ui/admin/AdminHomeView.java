    public AdminHomeView(MainFrame mainFrame) {
        initComponents();
        this.mainFrame = mainFrame;
        populateTable();

        bookTable.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int selectedRow = bookTable.getSelectedRow();
                if (selectedRow != -1) {
                    // Retrieve data from the selected row
                    String title = (String) bookTable.getValueAt(selectedRow, 0);
                    String author = (String) bookTable.getValueAt(selectedRow, 1);
                    double price = (double) bookTable.getValueAt(selectedRow, 2);

                    // Set the values to the corresponding text fields
                    lblTitle.setText(title);
                    lblAuthor.setText(author);
                    lblPrice.setText(String.valueOf(price));
                }
            }
        });
    }

    @Override
    public void populateTable() {
        System.out.println("Populating table...");
        // Get the book list
        List<Book> bookList = this.mainFrame.getStore().getBooks();

        // Populate the table with book data
        DefaultTableModel model = (DefaultTableModel) bookTable.getModel();
        for (Book book : bookList) {
            Object[] rowData = {
                book.getTitle(),
                book.getAuthor(),
                book.getPrice(),};
            model.addRow(rowData);
        }
    }

