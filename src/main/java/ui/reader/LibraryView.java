    @Override
    public void populateTable() {
        // your populating logic for this view..
        System.out.println("Populating library...");
        // Get the reader
        Reader currentReader = (Reader) DatabaseService.getCurrentUser();
        // Get the reader's library
        List<Book> library = currentReader.getLibrary();

        // Populate the table with book data
        DefaultTableModel model = (DefaultTableModel) bookTable.getModel();
        for (Book book : library) {
            Object[] rowData = {
                book.getTitle(),
                book.getAuthor(),
                book.getPrice(),};
            model.addRow(rowData);
        }
    }

    private Book getBookFromRow(int row) {
        DefaultTableModel model = (DefaultTableModel) bookTable.getModel();
        String selectedTitle = (String) model.getValueAt(row, 0); // Assuming title is in the first column
        Reader currentReader = (Reader) DatabaseService.getCurrentUser();
        // Get the reader's library
        List<Book> libraryBooks = currentReader.getLibrary();

        // search for the book
        for (Book book : libraryBooks) {
            if (book.getTitle().equals(selectedTitle)) {
                return book; // Found the matching book
            }
        }

        return null; // Book not found
    }

